export const getProductImage = (category, model, type) => {
    if (!PRODUCT_IMAGES[category]) return '';

    const categoryImages = PRODUCT_IMAGES[category];

    if (typeof categoryImages[model] === 'string') {
        return categoryImages[model];
    }

    if (categoryImages[model] && categoryImages[model][type]) {
        return categoryImages[model][type];
    }

    // Fallback to first available image in category
    const firstModel = Object.keys(categoryImages)[0];
    if (typeof categoryImages[firstModel] === 'string') {
        return categoryImages[firstModel];
    }
    const firstType = Object.keys(categoryImages[firstModel])[0];
    return categoryImages[firstModel][firstType] || '';
};
